package string;

public class OperasiString {
    public static void main(String[] args) {
        String kota = "Barabai";

        System.out.println(kota);

        char[] uniskaChar = { 'U', 'N', 'S', 'I', 'K', 'A'};
        String uniskaString = new String(uniskaChar);
        System.out.println(uniskaString);
    }
}
